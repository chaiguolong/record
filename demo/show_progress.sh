#!/usr/bin/env bash
# 编写脚本,显示进度条
# 思路:将进度条分为3部分
# 第一部分:[###########];打印一个左对齐的区域[],并不断覆盖'#'
# 第二部分:进度百分比,比如5%
# 第三部分:一个表示进度的旋转标识,用数组中的字符,循环表示

#1.前期准备,进度条字符'#';百分比数字;旋转字符'|\-/'.
i=0
str='#'
ch=('|' '\' '-' '/')
index=0
while [ $i -le 100 ]; do
    # -100s(左对齐100个字符串),%d(数字),%c(字符),%%(转移为单个%)
    printf "[%-100s][%d%%][%c]\r" $str $(($i)) ${ch[$index]}
    str+='#'
    let i++
    let index=i%4
    sleep 0.1
done
printf "\n"
echo "安装完成"
